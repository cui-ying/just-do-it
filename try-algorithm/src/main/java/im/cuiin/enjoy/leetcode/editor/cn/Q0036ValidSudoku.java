class Solution {
    public boolean isValidSudoku(char[][] board) {
        int[][] line = new int[board.length][board.length];
        int[][] column = new int[board.length][board.length];
        int[][] cell = new int[board.length][board.length];


        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board[i].length; j++) {
                if (board[i][j] == '.') {
                    continue;
                }

                int num = board[i][j] - '0' - 1;
                int k = i/3*3 + j/3;

                if (line[i][num] != 0 || column[j][num] != 0 || cell[k][num] != 0) {
                    return false;
                }
                line[i][num] = 1;
                column[j][num] = 1;
                cell[k][num] = 1;
            }
        }

        return true;
    }
}
